You can convert a PowerPoint presentation to standalone Flash (swf) slides with a third-party program. Each slide needs to be in a separate file.



<div class='heading2'>authorPOINT or authorPOINT LITE from <a target=_blank href='http://www.authorgen.com'>authorGEN</a></div>
<ul>

<li>Select the 'authorPOINT' tab from PowerPoint and then click 'Send Now'.</li>
<br>
<img src="images/authorPoint1.gif">
<br>

<li>Locate the exported Flash slides in 'Documents/authorGEN Projects/presentation name/aP Lite Flash/Slides'. Upload the swf slide files to My Library.  Hold down 'Shift' or 'Ctrl' key to select multiple files to upload.</li>

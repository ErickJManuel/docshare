A video file must be encoded in the FLV format before you can upload it to My Library.
There are many FLV video encoders that you can use. The instructions below provide some examples.

<div class='heading2'><a target=_blank href='http://rivavx.com'>Riva FLV Encoder</a></div>

<ul>
<li>Select an input video file. (AVI, MPEG, QuickTime, WMV)</li>
<li>Set Movie Size to 320x240. Keep the other default settings.</li>
<li>Choose an output directory for the destination video file.</li>
<li>Click the "FLV Encode" button and the file will be converted and saved to the output directory.</li>
<li>Find the resulting FLV file and upload it to My Library.</li>
</ul>

